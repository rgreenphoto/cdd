<p class="p35 ft20">Score Computations</p>
<p class="p36 ft21"><SpAN class="ft2">Combined Score - </SpAN>The Overall score is computed by Doubling the first round Freestyle score, and adding the first round</p>
<p class="p36 ft22">T&C score and doubling the Final round Freestyle score.</p>
<p class="p37 ft22"><SpAN class="ft23">Freestyle Score - </SpAN>Freestyle scores are determined by adding the score earned in each of the DISC categories and subtracting deductions. The catch ratio is not added to the score, but it is used to break ties.</p>
<p class="p35 ft20">Ties for 1st, 2nd, or 3rd place</p>
<p class="p38 ft22"><SpAN class="ft23">Open Overall - </SpAN>Ties are broken using the following criteria: 1) highest combined Catch Ratio from both Freestyle Rounds, 2) highest score from Throw & Catch Round 1.</p>
<p class="p39 ft22"><SpAN class="ft23">Open Freestyle - </SpAN>Ties are broken using the following criteria: 1) highest combined Catch Ratio from both Freestyle Rounds, 2) highest combined Comprehensive score from both Freestyle Rounds.</p>
<p class="p40 ft21"><SpAN class="ft2">Novice, Intermediate, and Open Throw & Catch - </SpAN>Ties are broken using the following criteria: 1) highest score in a single Throw & Catch round, 2) highest number of Bonus Zone completions.</p>
<p class="p41 ft21"><SpAN class="ft2">Throw & Catch Shootout - </SpAN>If the above criteria are not sufficient to break the tie, then the tied competitors will break the tie in a Throw & Catch Shootout. A coin toss determines which team goes first, and each team will be allowed one throw in each round of the shootout. The team that scores higher wins the shootout and is awarded the higher finish. Shootout rounds continue until one team outscores the other. Competitors will be allowed to rest their dogs as necessary.</p>
